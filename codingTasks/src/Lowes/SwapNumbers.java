package Lowes;

public class SwapNumbers {
	
	public static void main(String[] args) {
		
		int x = 5;
		int y = 10;
		int w = 0;
		
		w = x;
		x = y;
		y = w;
		
		
		System.out.println(x);
		System.out.println(y);
		
		System.out.println("-----------------------------");
		
		int a = 12;
		int b = 3;
		
		a = a + b;
		b = a - b;
		a = a - b;
		
		System.out.println(a);
		System.out.println(b);
		
		
		
		
		
	}

}
