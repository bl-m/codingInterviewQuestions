package namingConvention;

public class Patterns {
	
	public static void main(String[] args) {
		
		int a = 3;
		int b = 6;
		int temp = a;
		
	for(int i = 0; i < b; i++) {
		
		int sum = temp * a;
		a = sum;
		
		System.out.println(a);
		
		}
	
	System.out.println("------------------------------------------------------");
	
	
	}
}
